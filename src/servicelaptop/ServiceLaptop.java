    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String nama = txtNamaCustomer.getText();
        String merk = txtMerkLaptop.getText();
        String kerusakan = txtKerusakan.getText();

        if (nama.isEmpty() || merk.isEmpty() || kerusakan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
            return;
        }

        if (isEditMode) {
            // MODE UPDATE
            String sql = "UPDATE ServiceLaptop SET nama_customer=?, merk_laptop=?, kerusakan=? WHERE id=?";
            try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
                pstmt.setString(1, nama);
                pstmt.setString(2, merk);
                pstmt.setString(3, kerusakan);
                pstmt.setInt(4, editId);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate.");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal update data: " + e.getMessage());
            }
        } else {
            // MODE INSERT
            String sql = "INSERT INTO ServiceLaptop (nama_customer, merk_laptop, kerusakan) VALUES (?, ?, ?)";
            try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
                pstmt.setString(1, nama);
                pstmt.setString(2, merk);
                pstmt.setString(3, kerusakan);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
            }
        }

        clearForm();
        loadTableData();
        isEditMode = false;
        btnSimpan.setText("Simpan");
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void TabelServiceLaptopMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelServiceLaptopMouseClicked
        int row = TabelServiceLaptop.getSelectedRow();
        if (row >= 0) {
            int id = Integer.parseInt(TabelServiceLaptop.getValueAt(row, 0).toString());
            String nama = TabelServiceLaptop.getValueAt(row, 1).toString();
            String merk = TabelServiceLaptop.getValueAt(row, 2).toString();
            String kerusakan = TabelServiceLaptop.getValueAt(row, 3).toString();

            String[] options = {"Edit", "Hapus", "Batal"};
            int choice = JOptionPane.showOptionDialog(this, "Pilih aksi untuk data ini:",
                    "Pilihan Aksi", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE,
                    null, options, options[0]);

            if (choice == 0) { // Edit
                txtNamaCustomer.setText(nama);
                txtMerkLaptop.setText(merk);
                txtKerusakan.setText(kerusakan);
                editId = id;
                isEditMode = true;
                btnSimpan.setText("Update");

            } else if (choice == 1) { // Hapus
                int konfirmasi = JOptionPane.showConfirmDialog(this,
                        "Yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
                if (konfirmasi == JOptionPane.YES_OPTION) {
                    hapusData(id);
                }
            }
        }
    }//GEN-LAST:event_TabelServiceLaptopMouseClicked

    private void hapusData(int id) {
        String sql = "DELETE FROM ServiceLaptop WHERE id=?";
        try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setInt(1, id);
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
            loadTableData();
            clearForm();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
        }
    }

    private void loadTableData() {
        String[] kolom = {"ID", "Nama Customer", "Merk Laptop", "Kerusakan"};
        model = new DefaultTableModel(null, kolom);
        TabelServiceLaptop.setModel(model);

        String sql = "SELECT * FROM ServiceLaptop";
        try (Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery(sql)) {
            while (rs.next()) {
                int id = rs.getInt("id");
                String nama = rs.getString("nama_customer");
                String merk = rs.getString("merk_laptop");
                String kerusakan = rs.getString("kerusakan");

                String[] data = {
                    String.valueOf(id), nama, merk, kerusakan
                };
                model.addRow(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        }
    }

    private void clearForm() {
        txtNamaCustomer.setText("");
        txtMerkLaptop.setText("");
        txtKerusakan.setText("");
    }

